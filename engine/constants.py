def sipsin(day_stem: int, other_stem: int) -> str:
    """일간(day_stem) 기준 other_stem 의 십신."""
    de = CHEONGAN_OHAENG[day_stem]
    oe = CHEONGAN_OHAENG[other_stem]
    same_pol = CHEONGAN_EUMYANG[day_stem] == CHEONGAN_EUMYANG[other_stem]
    if de == oe:                       # 동일 오행 → 비겁
        return "비견" if same_pol else "겁재"
    if saeng(de) == oe:                # 일간이 생 → 식상
        return "식신" if same_pol else "상관"
    if geuk(de) == oe:                 # 일간이 극 → 재성
        return "편재" if same_pol else "정재"
    if geuk(oe) == de:                 # 일간을 극 → 관성
        return "편관" if same_pol else "정관"
    if saeng(oe) == de:                # 일간을 생 → 인성
        return "편인" if same_pol else "정인"
    raise AssertionError("unreachable")  # pragma: no cover


