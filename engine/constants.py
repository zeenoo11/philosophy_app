def sipiunseong(stem: int, branch: int, theory: str = "eumyang_sunyeok") -> str:
    """천간이 지지에서 갖는 십이운성.

    theory:
      eumyang_sunyeok  — 음양순역 (양간 순행, 음간 역행) [기본, 火土同宮]
      dongsaeng_dongsa — 동생동사 (음간도 같은 오행 양간처럼 순행)
      sutodonggung     — 수토동궁 (戊己를 壬癸 궁에 배속) + 음양순역
    """
    if theory == "sutodonggung":
        base = JANGSAENG_JI_SUTODONGGUNG[stem]
        forward = CHEONGAN_EUMYANG[stem] == 0
    elif theory == "dongsaeng_dongsa":
        # 음간을 같은 오행의 양간 장생/순행에 합류
        elem = CHEONGAN_OHAENG[stem]
        yang_stem = {0: 0, 1: 2, 2: 4, 3: 6, 4: 8}[elem]  # 목火土금수 양간
        base = JANGSAENG_JI[yang_stem]
        forward = True
    else:  # eumyang_sunyeok
        base = JANGSAENG_JI[stem]
        forward = CHEONGAN_EUMYANG[stem] == 0
    if forward:
        idx = (branch - base) % 12
    else:
        idx = (base - branch) % 12
    return SIPIUNSEONG[idx]


