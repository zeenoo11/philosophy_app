def jijanggan(branch: int, theory: str = "japyeongjinjeon") -> list[tuple[int, str]]:
    """지지의 지장간 목록 [(천간index, 역할), ...] — 정기가 마지막."""
    table = WORYULBUNYA_THEORIES[theory]
    return [(stem, role) for (stem, _days, role) in table[branch]]


def jeonggi(branch: int) -> int:
    """지지 정기(正氣=본기) 천간. 유파 무관(일수만 다름)."""
    for stem, _days, role in WORYULBUNYA_JAPYEONGJINJEON[branch]:
        if role == "정기":
            return stem
    raise ValueError(branch)


