def gz_name(gz60: int) -> str:
    """60갑자 인덱스 → 한글 이름 (예: '갑자')."""
    return CHEONGAN_HANGUL[gz60 % 10] + JIJI_HANGUL[gz60 % 12]


def gz_hanja(gz60: int) -> str:
    return CHEONGAN_HANJA[gz60 % 10] + JIJI_HANJA[gz60 % 12]


def gz_from(stem: int, branch: int) -> int:
    """천간/지지 → 60갑자 인덱스 (중국식 나머지정리)."""
    for k in range(60):
        if k % 10 == stem and k % 12 == branch:
            return k
    raise ValueError(f"invalid 간지 조합 stem={stem} branch={branch}")
